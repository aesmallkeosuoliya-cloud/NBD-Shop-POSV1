export function getTranslator(lang: Language) {
  return function t(key: string, replacements?: Record<string, string>): string {
    const keys = key.split('.');
    let result: string | Translations | undefined = translations[lang];
    for (const k of keys) {
      if (typeof result === 'object' && result !== null && k in result) {
        result = (result as Translations)[k];
      } else {
        result = undefined; // Key not found
        break;
      }
    }

    if (typeof result === 'string') {
      if (replacements) {
        return Object.entries(replacements).reduce((acc, [placeholder, value]) => {
          return acc.replace(new RegExp(`{${placeholder}}`, 'g'), value);
        }, result as string);
      }
      return result as string;
    }
    console.warn(`Translation key not found: ${key} for language: ${lang}`);
    return key; // Fallback to key if not found
  };
}
