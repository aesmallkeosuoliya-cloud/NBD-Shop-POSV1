export const getTranslator = (language: Language) => {
  const langTranslations = translations[language] || translations[Language.LO];

  // Helper to recursively find a translation for a dot-separated key
  const findTranslation = (keys: string[], trans: Translations | string): string | null => {
    const key = keys[0];
    if (typeof trans !== 'object' || trans === null || !trans.hasOwnProperty(key)) {
      return null;
    }
    if (keys.length === 1) {
      return typeof trans[key] === 'string' ? (trans[key] as string) : null;
    }
    return findTranslation(keys.slice(1), trans[key] as Translations);
  };

  return (key: string, replacements?: Record<string, string>): string => {
    const keys = key.split('.');
    let translation = findTranslation(keys, langTranslations);

    if (translation === null) {
      if (language !== Language.LO) {
        // Fallback to Lao for missing translations
        translation = findTranslation(keys, translations[Language.LO]);
      }

      if (translation === null) {
        console.warn(`Translation not found for key: "${key}" in language "${language}"`);
        return key; // Return the key itself if no translation is found
      }
    }
    
    if (replacements) {
      Object.keys(replacements).forEach(rKey => {
        translation = (translation as string).replace(`{${rKey}}`, replacements[rKey]);
      });
    }

    return translation as string;
  };
};